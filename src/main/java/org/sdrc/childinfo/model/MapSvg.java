package org.sdrc.childinfo.model;

public class MapSvg {

	private String d;
	private String fill;
	private String stroke;
	
	public String getD() {
		return d;
	}
	public void setD(String d) {
		this.d = d;
	}
	public String getFill() {
		return fill;
	}
	public void setFill(String fill) {
		this.fill = fill;
	}
	public String getStroke() {
		return stroke;
	}
	public void setStroke(String stroke) {
		this.stroke = stroke;
	}
	
}
