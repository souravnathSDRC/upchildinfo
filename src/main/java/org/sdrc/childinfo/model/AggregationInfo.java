package org.sdrc.childinfo.model;

public class AggregationInfo {
	
	private int iusNid;
	private int indicatorNid;
	private int unitNid;
	private int subgroupNid;
	private int areaNid;
	private int timePeriodNid;
	private int sourceNid;

	public int getIusNid() {
		return iusNid;
	}

	public void setIusNid(int iusNid) {
		this.iusNid = iusNid;
	}

	public int getIndicatorNid() {
		return indicatorNid;
	}

	public void setIndicatorNid(int indicatorNid) {
		this.indicatorNid = indicatorNid;
	}

	public int getUnitNid() {
		return unitNid;
	}

	public void setUnitNid(int unitNid) {
		this.unitNid = unitNid;
	}

	public int getSubgroupNid() {
		return subgroupNid;
	}

	public void setSubgroupNid(int subgroupNid) {
		this.subgroupNid = subgroupNid;
	}

	public int getAreaNid() {
		return areaNid;
	}

	public void setAreaNid(int areaNid) {
		this.areaNid = areaNid;
	}

	public int getTimePeriodNid() {
		return timePeriodNid;
	}

	public void setTimePeriodNid(int timePeriodNid) {
		this.timePeriodNid = timePeriodNid;
	}

	public int getSourceNid() {
		return sourceNid;
	}

	public void setSourceNid(int sourceNid) {
		this.sourceNid = sourceNid;
	}

}
